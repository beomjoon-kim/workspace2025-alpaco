def split_numbered_items(text: str):
    # "1. ...\n2. ...\n" í˜•ì‹ ë¶„ë¦¬
    import re
    items = []
    current = []
    for line in text.splitlines():
        if re.match(r"^\s*\d+\.\s+", line):
            if current:
                items.append("\n".join(current).strip())
                current = []
            current.append(re.sub(r"^\s*(\d+)\.\s+", "", line))
        else:
            if current:
                current.append(line)
    if current:
        items.append("\n".join(current).strip())
    return [i for i in items if i]

